# Type casting

# it is used to convert one data type to another data type
# and also it is a function not function

# there are two types

# 1 Implicit type casting

a = 1234
b = "1234"
print( a + int(b))
# when we convert data type manually , it's consider as Implicity conversion

# 2 Explicit type casting

a = 1.234
b = 4
print(a + b)
# it converts data type automatically by python interpreter , it's consiter as Explicity conversion